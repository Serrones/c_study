/*
Preprocessor command, which tells a C compiler to include
stdio.h file before going to actual compilation
*/
#include <stdio.h>

/* main function where program execution begins */
int main()
{
    /* 
    printf is a function available in C which causes the message
    "Hello, World!" to be displayed on the screen
    */
    printf("Hello, World!\n");

    /* Terminates main()function and returns the value 0 */
    return 0;
}